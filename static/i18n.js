export function detectLang() {
  const saved = localStorage.getItem("clip_lang");
  if (saved && STRINGS[saved]) return saved;
  const nav = (navigator.language || "en").slice(0, 2).toLowerCase();
  return STRINGS[nav] ? nav : "en";
}
